package com.almundo.automation.entities;

public class OneKey {

	private String one;
	private String key;

	public OneKey(String one, String key) {
		this.one = one;
		this.key = key;
	}

	public String getOne() {
		return one;
	}

	public void setOne(String one) {
		this.one = one;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

}
